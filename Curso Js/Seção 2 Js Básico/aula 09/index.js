
// Diferenças entre Var e Let

// var pode ser substituido o nome da variável
var nome = 'Francisco'
nome = 'Júnior'
console.log(nome)

//NÃO PODE TER LET COM O MESMO NOME
// CASO QUEIRA ALTERAR NOME DA VARIÁVEL

let nome = 'Francisco'
// let nome = 'Júnior' ( não pode let com o mesmo nome)
nome = 'Junior' // ta ok pode alterar o nome da variável

console.log(nome)
