/*
Operadores Lógicos
---------------------------------------------------

&& -> AND -> E    -> Todas as expressões precisam ser verdadeiras para retornar true "verdadeira" 

const expressaoAnd = true && true && true && true
console.log(expressaoAnd)
-----------------------------------------------------

|| -> OR -> OU   -> Basta uma expressão ser true para retornar true caso seja todas false retorna false

const expressaoOr = true || true || true || true
console.log(expressaoOr)
-----------------------------------------------------

! -> NOT -> NÃO  -> (!true se torna false)  (!false se torna true)
 (!! true se torna false e volta true) 


*/